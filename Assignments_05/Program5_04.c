/* 
    4. Write a program which accepts N from user and print all odd numbers up to N.

    Input  : 18

    Output : 1 3 5 7 9 11 13 15 17

*/

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   OddDisplay
//  Description     :   Displays all odd numbers from 1 to N.
//  Input           :   Integer
//  Output          :   Integer(Prints odd numbers on console.)
//  Author          :   Gavade Somanath Vilas
//  Date            :   05/01/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//  Function Definition
void OddDisplay(int iNo)
{
    int iCnt = 0;

    //  Updator
    if(iNo < 0)
    {
        iNo = -(iNo);
    }

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if(iCnt % 2 != 0)
        {
            printf("%d\t", iCnt);
        }
        
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Display Odd Numbers.
//  Description         :   This application accepts one integer from the user and displays all odd numbers up to that number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a Number : \n");
    scanf("%d", &iValue);

    OddDisplay(iValue);     //  Function Call

    return 0;
}