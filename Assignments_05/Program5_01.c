/*
    1.Write a program which accept number from user and print that number of( $ & * )on screen.

    Input   : 5
    Output  : $ * $ * $ * $ * $ *

    Input   : 3
    Output  : $ * $ * $ *

    Input   : -3
    Output  : $ * $ * $ *

*/

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   Pattern
//  Description     :   Prints "$ *" pattern for the given number of times.
//  Input           :   Integer
//  Output          :   Prints pattern on console.
//  Author          :   Gavade Somanath Vilas
//  Date            :   05/01/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//  Function Definition
void Pattern(int iNo)
{
    int iCnt = 0;

    //  Updator
    if(iNo < 0)
    {
        iNo = -(iNo);
    }

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("$\t*\t");
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Pattern Printing.
//  Description         :   This application accepts one integer from the user and prints "$ *" pattern that many times.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a Number : \n");
    scanf("%d", &iValue);

    Pattern(iValue);        //  Function Call

    return 0;
}