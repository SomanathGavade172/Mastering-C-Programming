/* 
    5. Write a program which accept N and print first 5 multiples of N.

    Input  : 4

    Output : 4 8 12 16 20

*/

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   MultipleDisplay
//  Description     :   Displays first five multiples of the given number.
//  Input           :   Integer
//  Output          :   Integer(Prints multiples on console.)
//  Author          :   Gavade Somanath Vilas
//  Date            :   05/01/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//  Function Definition
void MultipleDisplay(int iNo)
{
    int iCnt = 0;

    //  Updator
    if(iNo < 0)
    {
        iNo = -(iNo);
    }

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        printf("%d\t", iNo * iCnt);
    }    
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Display First Five Multiples
//  Description         :   This application accepts one integer from the user and displays the first five multiples of that number.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a Number : \n");
    scanf("%d", &iValue);

    MultipleDisplay(iValue);        //  Function Call

    return 0;
}