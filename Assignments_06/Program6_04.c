#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   Table
//  Description     :   Displays the multiplication table of a given number.
//  Input           :   Integer
//  Output          :   None(Displays table on console)
//  Author          :   Gavade Somanath Vilas
//  Date            :   06/01/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//  Function Definition
void Table(int iNo)
{
    int iCnt = 0;

    //  Filter
    if(iNo == 0)
    {
        return;
    }

    //  Updator
    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for(iCnt = 1; iCnt <= 10; iCnt++)
    {
        printf("%d\t", iNo * iCnt);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Display Table
//  Description         :   This application accepts one integer from the user and displays its multiplication table.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter a Number : \n");
    scanf("%d", &iValue);

    Table(iValue);      //  Function Call.

    return 0;
}