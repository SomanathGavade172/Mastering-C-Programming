/*  
    5. Write a program which accept area in square feet and convert it into square meter. (1 square feet = 0.0929 Square meter)

    Input  : 5
    Output : 0.464515

    Input  : 7
    Output : 0.650321

*/

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   SquareMeter
//  Description     :   Converts area from square feet to square meters.
//  Input           :   Integer
//  Output          :   Double (Equivalent area in square meters)
//  Author          :   Gavade Somanath Vilas
//  Date            :   09/01/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//  Function Definition
double SquareMeter(int iValue)
{
    double dConvert = 0;

    dConvert = iValue * 0.0929;

    return dConvert;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Square Feet to Square Meter Conversion
//  Description         :   This application accepts area in square feet from the user and converts it into square meters.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    double dRet = 0.0;

    printf("Enter a number : ");
    scanf("%d", &iValue);

    dRet = SquareMeter(iValue);     //  Function call.

    printf("%f square meters", dRet);

    return 0;
}