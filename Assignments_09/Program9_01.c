/* 
    1.Write a program which accept radius of circle from user and calculate its area.
    Consider value of PI as 3.14. (Area = PI * Radius * Radius).

    Input  : 5.3
    Output : 88.2026

    Input  : 10.4
    Output : 339.6224

*/

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   CircleArea
//  Description     :   Calculates the area of a circle using the given radius.
//  Input           :   Float
//  Output          :   Double (Calculated area of the circle)
//  Author          :   Gavade Somanath Vilas
//  Date            :   09/01/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//  Function Definition
double CircleArea(float fRadius)
{
    double dArea = 0.0;
    float fPI = 3.14f;

    dArea = fPI * fRadius * fRadius;

    return dArea;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Circle Area Calculation
//  Description         :   This application accepts the radius of a circle from the user and calculates its area using the formula PI * R * R.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0f;
    double dRet = 0.0f;

    printf("Enter a Radius : \n");
    scanf("%f", &fValue);

    dRet = CircleArea(fValue);      //  Function call

    printf("Area of a Circle is : %f\n", dRet);

    return 0;
}