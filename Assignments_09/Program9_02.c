#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   RectArea
//  Description     :   Calculates the area of a rectangle using width and height.
//  Input           :   Float
//  Output          :   Double (Calculated area of the rectangle)
//  Author          :   Gavade Somanath Vilas
//  Date            :   09/01/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//  Function Definition
double RectArea(float fWidth, float fHeight)
{
    double dArea = 0.0;

    dArea = fWidth * fHeight;

    return dArea;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Rectangle Area Calculation
//  Description         :   This application accepts width and height of a rectangle from the user and calculates its area.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue1 = 0.0f, fValue2 = 0.0f;
    double dRet = 0.0;

    printf("Enter Width : \n");
    scanf("%f", &fValue1);

    printf("Enter Height : \n", &fValue2);
    scanf("%f", &fValue2);

    dRet = RectArea(fValue1, fValue2);      //  Function Call.

    printf("Area of a Reactangle is : %f\n", dRet);

    return 0;
}