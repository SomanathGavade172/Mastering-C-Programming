/* 
    3. Write a program which accept distance in kilometre and convert it into meter. (1 kilometre = 1000 Meter)

    Input  : 5
    Output : 5000

    Input  : 12
    Output : 12000

*/
#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   :   KMtoMeter
//  Description     :   Converts distance from kilometre to meter.
//  Input           :   Integer
//  Output          :   Integer (Equivalent distance in meter)
//  Author          :   Gavade Somanath Vilas
//  Date            :   08/01/2026
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//  Function Definition
int KMtoMeter(int iKM)
{
    int iConvert = 0;

    iConvert = iKM * 1000;

    return iConvert;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Application Name    :   Kilometre to Meter Conversion
//  Description         :   This application accepts distance in kilometre from the user and converts it into meter.
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter a distance : ");
    scanf("%d", &iValue);

    iRet =  KMtoMeter(iValue);      //  Function call.

    printf("%d KM is = %d Meter\n", iValue, iRet);

    return 0;
}
